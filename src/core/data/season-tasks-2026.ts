/**
 * Season Tasks — 2026
 *
 * All planned tasks for the 2026 growing season.
 * Organized chronologically by phase. Each task is either 10M or 30M.
 * Anything that would take longer than 30 minutes is broken into subtasks.
 *
 * This is seed data for the task database. Human-scannable, machine-loadable.
 * Recurring tasks use the recurrence field — the task engine expands them
 * into individual instances at generation time.
 *
 * Tier: '10M' = ~10 minutes, '30M' = ~30 minutes.
 * Fatigue: 'exertion' (gross motor), 'precision' (fine motor/posture), 'monitoring' (cognitive).
 */

// ── Types ───────────────────────────────────────────────────────────────────

export type TaskTier = '10M' | '30M';
export type FatigueType = 'exertion' | 'precision' | 'monitoring';

export type TaskCategory =
  | 'infrastructure'
  | 'seed_starting'
  | 'deterrence'
  | 'planting'
  | 'maintenance'
  | 'harvest'
  | 'processing'
  | 'cleanup';

export interface TaskRecurrence {
  interval_days: number;
  end_date: string; // ISO date
}

export interface SeasonTask {
  id: string;
  name: string;
  category: TaskCategory;
  species_id?: string;
  target_date: string; // ISO date
  tier: TaskTier;
  fatigue_type: FatigueType;
  equipment: string[];
  plant_count?: number;
  estimated_minutes: number;
  depends_on?: string[];
  recurrence?: TaskRecurrence;
  notes?: string;
}

